package petrock;

public class RandomEvent {
  
  // Event trigger
  public void triggerEvent(PetRock rock){ // Randomly selects and triggers a positive or negative event.

  }

  // Positive event
  public void shinyPebbleEvent(PetRock rock){ // Improves the rock's mood.

  }

  public void extraSleepEvent(PetRock rock){ // Restores the rock's energy.

  }


  // Negative event
  public void suddenNoiseEvent(PetRock rock){ // Increases the rock's boredom.

  }

  
  public void grumpyEvent(PetRock rock){ // Increases the rock's hunger.  
  }





}
